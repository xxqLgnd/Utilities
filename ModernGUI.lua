-- –§—É–Ω–∫—Ü–∏—è –ø–æ–ª–µ—Ç–∞
local flyConnection = nil
local flySpeed = 50

local function toggleFly()
    if not flyEnabled then
        if LocalPlayer.Character and LocalPlayer.Character:FindFirstChild("HumanoidRootPart") then
            local bodyVelocity = Instance.new("BodyVelocity")
            bodyVelocity.MaxForce = Vector3.new(4000, 4000, 4000)
            bodyVelocity.Velocity = Vector3.new(0, 0, 0)
            bodyVelocity.Parent = LocalPlayer.Character.HumanoidRootPart
            
            flyConnection = RunService.Heartbeat:Connect(function()
                if LocalPlayer.Character and LocalPlayer.Character:FindFirstChild("HumanoidRootPart") then
                    local camera = game.Workspace.CurrentCamera
                    local moveVector = LocalPlayer.Character.Humanoid.MoveDirection
                    local lookVector = camera.CFrame.LookVector
                    local rightVector = camera.CFrame.RightVector
                    
                    local velocity = Vector3.new(0, 0, 0)
                    
                    -- –£–ø—Ä–∞–≤–ª–µ–Ω–∏–µ –∫–ª–∞–≤–∏—à–∞–º–∏
                    if UserInputService:IsKeyDown(Enum.KeyCode.W) then
                        velocity = velocity + lookVector
                    end
                    if UserInputService:IsKeyDown(Enum.KeyCode.S) then
                        velocity = velocity - lookVector
                    end
                    if UserInputService:IsKeyDown(Enum.KeyCode.A) then
                        velocity = velocity - rightVector
                    end
                    if UserInputService:IsKeyDown(Enum.KeyCode.D) then
                        velocity = velocity + rightVector
                    end
                    if UserInputService:IsKeyDown(Enum.KeyCode.Space) then
                        velocity = velocity + Vector3.new(0, 1, 0)
                    end
                    if UserInputService:IsKeyDown(Enum.KeyCode.LeftShift) then
                        velocity = velocity - Vector3.new(0, 1, 0)
                    end
                    
                    bodyVelocity.Velocity = velocity * flySpeed
                else
                    if flyConnection then
                        flyConnection:Disconnect()
                        flyConnection = nil
                    end
                end
            end)
            
            flyEnabled = true
            flyButton.Text = "ü¶Ö –û—Ç–∫–ª—é—á–∏—Ç—å"
            flyButton.BackgroundColor3 = Color3.fromRGB(255, 85, 85)
            updateInfo("–ü–æ–ª–µ—Ç –∞–∫—Ç–∏–≤–∏—Ä–æ–≤–∞–Ω! WASD + Space/Shift")
        else
            updateInfo("–û—à–∏–±–∫–∞: –ø–µ—Ä—Å–æ–Ω–∞–∂ –Ω–µ –Ω–∞–π–¥–µ–Ω!")
        end
    else
        if flyConnection then
            flyConnection:Disconnect()
            flyConnection = nil
        end
        
        if LocalPlayer.Character and LocalPlayer.Character:FindFirstChild("HumanoidRootPart") then
            local bodyVelocity = LocalPlayer.Character.HumanoidRootPart:FindFirstChild("BodyVelocity")
            if bodyVelocity then
                bodyVelocity:Destroy()
            end
        end
        
        flyEnabled = false
        flyButton.Text = "ü¶Ö –ü–æ–ª–µ—Ç"
        flyButton.BackgroundColor3 = Color3.fromRGB(170, 85, 255)
        updateInfo("–ü–æ–ª–µ—Ç –æ—Ç–∫–ª—é—á–µ–Ω")
    end
end

-- –§—É–Ω–∫—Ü–∏—è –Ω–µ–≤–∏–¥–∏–º–æ—Å—Ç–∏
local function toggleInvisibility()
    if LocalPlayer.Character then
        if not invisEnabled then
            -- –î–µ–ª–∞–µ–º –Ω–µ–≤–∏–¥–∏–º—ã–º
            for _, part in pairs(LocalPlayer.Character:GetChildren()) do
                if part:IsA("BasePart") and part.Name ~= "HumanoidRootPart" then
                    part.Transparency = 1
                elseif part:IsA("Accessory") and part:FindFirstChild("Handle") then
                    part.Handle.Transparency = 1
                end
            end
            
            -- –°–∫—Ä—ã–≤–∞–µ–º –ª–∏—Ü–æ
            if LocalPlayer.Character:FindFirstChild("Head") and LocalPlayer.Character.Head:FindFirstChild("face") then
                LocalPlayer.Character.Head.face.Transparency = 1
            end
            
            invisEnabled = true
            invisButton.Text = "üëª –í–∏–¥–∏–º—ã–π"
            invisButton.BackgroundColor3 = Color3.fromRGB(255, 85, 85)
            updateInfo("–ù–µ–≤–∏–¥–∏–º–æ—Å—Ç—å –∞–∫—Ç–∏–≤–∏—Ä–æ–≤–∞–Ω–∞!")
        else
            -- –î–µ–ª–∞–µ–º –≤–∏–¥–∏–º—ã–º
            for _, part in pairs(LocalPlayer.Character:GetChildren()) do
                if part:IsA("BasePart") and part.Name ~= "HumanoidRootPart" then
                    part.Transparency = 0
                elseif part:IsA("Accessory") and part:FindFirstChild("Handle") then
                    part.Handle.Transparency = 0
                end
            end
            
            -- –ü–æ–∫–∞–∑—ã–≤–∞–µ–º –ª–∏—Ü–æ
            if LocalPlayer.Character:FindFirstChild("Head") and LocalPlayer.Character.Head:FindFirstChild("face") then
                LocalPlayer.Character.Head.face.Transparency = 0
            end
            
            invisEnabled = false
            invisButton.Text = "üëª –ù–µ–≤–∏–¥–∏–º–æ—Å—Ç—å"
            invisButton.BackgroundColor3 = Color3.fromRGB(85, 255, 170)
            updateInfo("–ù–µ–≤–∏–¥–∏–º–æ—Å—Ç—å –æ—Ç–∫–ª—é—á–µ–Ω–∞")
        end
    else
        updateInfo("–û—à–∏–±–∫–∞: –ø–µ—Ä—Å–æ–Ω–∞–∂ –Ω–µ –Ω–∞–π–¥–µ–Ω!")
    end
end

